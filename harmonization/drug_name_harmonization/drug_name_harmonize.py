def extract_interventions_from_study(study: dict) -> list:
    """
    Extract drug/biological interventions from a single study.

    SOURCE FIELD: protocolSection.armsInterventionsModule.interventions[]
    Uses:
        - interventions[].name       → primary drug name
        - interventions[].type       → filter DRUG/BIOLOGICAL only
        - interventions[].otherNames → sponsor-provided synonyms/aliases

    Args:
        study: A single study dict from the CT.gov API response.

    Returns:
        List of dicts: [{"name": str, "other_names": [str], "nct_id": str}]
    """
    protocol = study.get("protocolSection", {})
    nct_id = protocol.get("identificationModule", {}).get("nctId", "UNKNOWN")
    arms_module = protocol.get("armsInterventionsModule", {})
    interventions = arms_module.get("interventions", [])

    results = []
    for intv in interventions:
        intv_type = intv.get("type", "").upper()
        intv_name = intv.get("name", "").strip()
        other_names = intv.get("otherNames", [])

        # Only DRUG and BIOLOGICAL types
        if intv_type not in DRUG_TYPES:
            continue

        # Skip placebo interventions
        if not intv_name or is_placebo(intv_name):
            continue

        # Clean synonyms — remove empty strings and placebos
        clean_others = [
            n.strip() for n in other_names
            if n.strip() and not is_placebo(n)
        ]

        results.append({
            "name": intv_name,
            "other_names": clean_others,
            "nct_id": nct_id,
        })

    return results


def extract_mesh_synonym_pairs(study: dict) -> list:
    """
    Extract NLM-derived MeSH synonym pairs from a study's derived section.

    SOURCE FIELD: derivedSection.interventionBrowseModule
    Uses:
        - browseLeaves[].name     → MeSH concept name (standardized)
        - browseLeaves[].asFound  → Original text that matched (study-submitted)
        - browseLeaves[].relevance→ "HIGH" or "LOW" (we only use HIGH)

    This gives us pairs like:
        ("Metformin Hydrochloride", "Metformin") — asFound ↔ MeSH name
    which creates additional synonym edges for the Union-Find.

    Args:
        study: A single study dict from the CT.gov API response.

    Returns:
        List of tuples: [(original_text, mesh_term), ...]
    """
    derived = study.get("derivedSection", {})
    browse_module = derived.get("interventionBrowseModule", {})
    browse_leaves = browse_module.get("browseLeaves", [])

    pairs = []
    for leaf in browse_leaves:
        relevance = leaf.get("relevance", "").upper()
        if relevance != "HIGH":
            continue

        mesh_name = leaf.get("name", "").strip()
        as_found = leaf.get("asFound", "").strip()

        if mesh_name and as_found and mesh_name.lower() != as_found.lower():
            # Don't create pairs for non-drug MeSH terms
            if not is_placebo(mesh_name) and not is_placebo(as_found):
                pairs.append((as_found, mesh_name))

    return pairs


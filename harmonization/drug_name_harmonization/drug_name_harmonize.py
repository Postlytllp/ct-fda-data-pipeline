API_BASE = "https://clinicaltrials.gov/api/v2/studies"

# Fields to request from the API — kept minimal for speed
# Source 1: Study-submitted intervention data (name, type, otherNames)
# Source 2: NLM-derived MeSH terms (standardized drug vocabulary)
API_FIELDS = [
    "protocolSection.identificationModule.nctId",
    "protocolSection.armsInterventionsModule.interventions",
    "derivedSection.interventionBrowseModule",
]

DRUG_TYPES = {"DRUG", "BIOLOGICAL"}

PLACEBO_KEYWORDS = {"placebo", "placebos", "sham", "placebo comparator"}


def is_placebo(name: str) -> bool:
    """Check if an intervention name is a placebo/sham."""
    name_lower = name.strip().lower()
    if name_lower in PLACEBO_KEYWORDS:
        return True
    if "placebo" in name_lower or "sham" in name_lower:
        return True
    return False


def fetch_and_deduplicate_drugs(
    max_pages: Optional[int] = None,
    page_size: int = 1000,
    status_filter: Optional[list] = None,
    include_mesh: bool = True,
    g_model=None,              # ← add
    gliner_batch_size: int = 256,  # ← add
    output_dir: str = ".",
    json_filename: str = "ctgov_drugs.json",
    csv_filename: str = "ctgov_drugs.csv",
    trace_drug: Optional[str] = None,
    verbose: bool = True,
) -> tuple:
    """
    End-to-end pipeline: Fetch → Extract → Deduplicate → Save.

    Data sources extracted per study:
    ─────────────────────────────────
    1. protocolSection.armsInterventionsModule.interventions[]
       ├── .name       → Primary drug name
       ├── .type       → Filter: DRUG or BIOLOGICAL only
       └── .otherNames → Sponsor-provided synonyms
    2. derivedSection.interventionBrowseModule.browseLeaves[]
       ├── .asFound    → Original text from study
       ├── .name       → NLM-standardized MeSH term
       └── .relevance  → Only HIGH relevance used

    Args:
        max_pages: Max API pages to fetch (None = ALL trials).
                   Use max_pages=5 for quick testing (~5000 studies).
        page_size: Studies per API page (max 1000).
        status_filter: Optional list like ['RECRUITING', 'COMPLETED'].
        include_mesh: Include NLM-derived MeSH synonyms (recommended).
        output_dir: Where to save output files.
        json_filename: Name for JSON output.
        csv_filename: Name for CSV output.
        verbose: Print progress updates.

    Returns:
        (df, drugs_list):
            - df: pandas DataFrame [canonical_name, synonyms, synonym_count, synonyms_display]
            - drugs_list: list of dicts (same as saved JSON)

    Example (Google Colab):
        # Quick test (~5000 studies):
        df, drugs = fetch_and_deduplicate_drugs(max_pages=5)

        # Full extraction (all trials, 15-30+ min):
        df, drugs = fetch_and_deduplicate_drugs()

        # Only recruiting trials:
        df, drugs = fetch_and_deduplicate_drugs(status_filter=['RECRUITING'])

        # Without MeSH synonyms (faster, fewer synonyms):
        df, drugs = fetch_and_deduplicate_drugs(include_mesh=False)
    """
    start_time = time.time()

    # Step 1: Fetch all interventions and MeSH pairs
    interventions, mesh_pairs = fetch_all_interventional_drugs(
        max_pages=max_pages,
        page_size=page_size,
        status_filter=status_filter,
        include_mesh=include_mesh,
        verbose=verbose,
    )

    if not interventions:
        print("No interventions found!")
        return pd.DataFrame(), []

    # Step 2: Deduplicate with Union-Find
    groups = deduplicate_drugs(
    interventions,
    mesh_pairs=mesh_pairs if include_mesh else None,
    verbose=verbose,
    trace_drug=trace_drug,
)

    # Step 3: Save to files
    json_path, csv_path, records = save_results(
        groups,
        output_dir=output_dir,
        json_filename=json_filename,
        csv_filename=csv_filename,
        verbose=verbose,
    )

    # Step 4: Create DataFrame
    df = create_dataframe(records)

    # Step 4b: GLiNER extraction + second-pass merge
    if g_model is not None:
        if verbose:
            print("\nRunning GLiNER drug name extraction...")
        df = extract_drug_names(df, g_model, batch_size=gliner_batch_size, verbose=verbose)

        if verbose:
            print("Merging by extracted drug names (exact match)...")
        df = merge_by_extracted_drugs(df, verbose=verbose)

        if verbose:
            print("Merging by subset drug names...")
        df = merge_by_subset_drugs(df, verbose=verbose)

    # Step 5: Summary
    elapsed = time.time() - start_time
    if verbose:
        print_summary(df, elapsed)

    # Step 6: Auto-download in Colab
    download_in_colab(json_path, csv_path, verbose=verbose)

    return df, records


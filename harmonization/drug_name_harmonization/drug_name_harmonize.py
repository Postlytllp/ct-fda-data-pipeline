class UnionFind:
    """
    Union-Find (Disjoint Set Union) with path compression and union by rank.
    Earlier-seen names become canonical representatives.
    All matching is case-insensitive, but original casing is preserved.
    """

    def __init__(self):
        self.parent = {}       # normalized_name -> normalized_parent
        self.rank = {}         # normalized_name -> rank
        self.original = {}     # normalized_name -> first-seen original casing
        self.first_seen = {}   # normalized_name -> insertion order
        self._counter = 0

    @staticmethod
    def _normalize(name: str) -> str:
        return name.strip().lower()

    def add(self, name: str):
        """Add a name if not already present."""
        norm = self._normalize(name)
        if norm not in self.parent:
            self.parent[norm] = norm
            self.rank[norm] = 0
            self.original[norm] = name.strip()
            self.first_seen[norm] = self._counter
            self._counter += 1

    def find(self, name: str) -> str:
        """Find root representative (with path compression)."""
        norm = self._normalize(name)
        if norm not in self.parent:
            self.add(name)
        return self._find_norm(norm)

    def _find_norm(self, norm: str) -> str:
        """Find root for already-normalized key (path compression)."""
        if self.parent[norm] != norm:
            self.parent[norm] = self._find_norm(self.parent[norm])
        return self.parent[norm]

    def union(self, name_a: str, name_b: str):
        """Merge two sets. Earlier-seen name becomes root."""
        root_a = self.find(name_a)
        root_b = self.find(name_b)
        if root_a == root_b:
            return

        # Union by rank, preferring earlier-seen as root
        if self.rank[root_a] < self.rank[root_b]:
            root_a, root_b = root_b, root_a
        if self.first_seen[root_b] < self.first_seen[root_a]:
            root_a, root_b = root_b, root_a
            if self.rank[root_a] == self.rank[root_b]:
                self.rank[root_a] += 1

        self.parent[root_b] = root_a
        if self.rank[root_a] == self.rank[root_b]:
            self.rank[root_a] += 1

    def get_groups(self) -> dict:
        """Returns {canonical_name: sorted list of all names in group}."""
        groups = defaultdict(set)
        for norm in self.parent:
            root = self._find_norm(norm)
            groups[root].add(self.original[norm])

        return {
            self.original[root_norm]: sorted(members, key=lambda x: x.lower())
            for root_norm, members in groups.items()
        }


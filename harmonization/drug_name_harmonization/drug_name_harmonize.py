def merge_by_subset_drugs(df: pd.DataFrame, verbose: bool = True) -> pd.DataFrame:
    """
    Third-pass deduplication:
    For rows with drug_extracted length > 1, check if ALL extracted names
    are a subset of another row's synonyms. If so, merge them.
    Canonical name = shorter name (fewer characters).
    """
    from collections import defaultdict

    # Build synonym lookup: lowered synonym -> list of row indices
    syn_to_rows = defaultdict(set)
    for i, row in df.iterrows():
        for s in row["synonyms"]:
            syn_to_rows[s.strip().lower()].add(i)

    rows_to_drop = []
    already_merged = set()

    for i, row in df.iterrows():
        if i in already_merged:
            continue
        if len(row["drug_extracted"]) <= 1:
            continue

        extracted_lower = [d.strip().lower() for d in row["drug_extracted"]]

        # Find rows that contain ALL extracted drugs in their synonyms
        candidate_sets = [syn_to_rows.get(d, set()) for d in extracted_lower]
        if not candidate_sets:
            continue

        # Intersect: rows that have every extracted drug in synonyms
        common_rows = candidate_sets[0].copy()
        for s in candidate_sets[1:]:
            common_rows &= s

        # Remove self
        common_rows.discard(i)
        # Remove already merged
        common_rows -= already_merged

        if not common_rows:
            continue

        # Pick the best target: prefer the one whose canonical is shortest
        target_idx = min(common_rows, key=lambda x: len(df.at[x, "canonical_name"]))

        # Decide canonical: shorter name wins
        src_canonical = df.at[i, "canonical_name"]
        tgt_canonical = df.at[target_idx, "canonical_name"]
        new_canonical = src_canonical if len(src_canonical) < len(tgt_canonical) else tgt_canonical

        # Merge synonyms + nct_ids
        combined_syns = []
        combined_ncts = []
        seen = set()

        for idx in [target_idx, i]:
            for s, n in zip(df.at[idx, "synonyms"], df.at[idx, "nct_ids"]):
                if s.lower() not in seen:
                    seen.add(s.lower())
                    combined_syns.append(s)
                    combined_ncts.append(n)

        df.at[target_idx, "canonical_name"] = new_canonical
        df.at[target_idx, "synonyms"] = combined_syns
        df.at[target_idx, "nct_ids"] = combined_ncts
        df.at[target_idx, "synonym_count"] = len(combined_syns)

        rows_to_drop.append(i)
        already_merged.add(i)

    df = df.drop(rows_to_drop).reset_index(drop=True)

    # Refresh display columns
    df["synonyms_display"] = df["synonyms"].apply(lambda x: " | ".join(x))
    df["nct_ids_display"] = df["nct_ids"].apply(lambda x: " | ".join(x))

    if verbose:
        print(f"  Subset merge: dropped {len(rows_to_drop)} rows")
        print(f"  Remaining: {len(df)} unique drug entities")

    return df


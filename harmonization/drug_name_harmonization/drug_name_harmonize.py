def fetch_all_interventional_drugs(
    max_pages: Optional[int] = None,
    page_size: int = 1000,
    status_filter: Optional[list] = None,
    include_mesh: bool = True,
    verbose: bool = True,
) -> tuple:
    """
    Fetch all drug interventions from ClinicalTrials.gov v2 API.

    Iterates through all pages, extracting:
    1. Study-submitted drug names + otherNames (from protocolSection)
    2. NLM-derived MeSH synonym pairs (from derivedSection)

    Args:
        max_pages: Max pages to fetch (None = all). Each page ≈ 1000 studies.
        page_size: Studies per API call (max 1000).
        status_filter: Optional list of statuses e.g. ['RECRUITING'].
        include_mesh: Also extract MeSH-derived synonym pairs.
        verbose: Print progress.

    Returns:
        (interventions, mesh_pairs):
            - interventions: list of {"name", "other_names", "nct_id"} dicts
            - mesh_pairs: list of (original_text, mesh_term) tuples
    """
    all_interventions = []
    all_mesh_pairs = []
    page_token = None
    page_num = 0
    total_studies = 0

    params = build_api_params(page_size, status_filter)

    if verbose:
        print("=" * 70)
        print("ClinicalTrials.gov Drug Name Extractor")
        print("=" * 70)
        print(f"Fetching interventional trials (page_size={page_size})...")
        print(f"Fields requested:")
        for f in API_FIELDS:
            print(f"  • {f}")
        print()

    while True:
        if max_pages is not None and page_num >= max_pages:
            if verbose:
                print(f"\nReached max_pages limit ({max_pages}). Stopping.")
            break

        data = fetch_page(params, page_token, verbose=verbose)
        studies = data.get("studies", [])
        total_studies += len(studies)
        page_num += 1

        # Extract from each study
        for study in studies:
            # Source 1: Study-submitted interventions (name + otherNames)
            study_drugs = extract_interventions_from_study(study)
            all_interventions.extend(study_drugs)

            # Source 2: NLM-derived MeSH synonym pairs
            if include_mesh:
                mesh_pairs = extract_mesh_synonym_pairs(study)
                all_mesh_pairs.extend(mesh_pairs)

        if verbose:
            total_count = data.get("totalCount", "?")
            print(
                f"  Page {page_num}: {len(studies)} studies | "
                f"Total: {total_studies} | "
                f"API total: {total_count} | "
                f"Drugs: {len(all_interventions)} | "
                f"MeSH pairs: {len(all_mesh_pairs)}"
            )

        # Next page
        page_token = data.get("nextPageToken")
        if not page_token:
            if verbose:
                print(f"\nDone! Fetched {total_studies} studies across {page_num} pages.")
            break

        time.sleep(0.3)  # Be respectful to the API

    return all_interventions, all_mesh_pairs

from collections import defaultdict

def deduplicate_drugs(
    interventions: list,
    mesh_pairs: Optional[list] = None,
    verbose: bool = True,
    trace_drug: Optional[str] = None,
) -> dict:

  groups_raw = defaultdict(dict)  # key -> {normalized_name: (original_name, nct_id)}

  for intv in interventions:
      key = intv["name"].strip().lower()
      name = intv["name"].strip()
      nct_id = intv["nct_id"]

      if key not in groups_raw[key]:
          groups_raw[key][key] = (name, nct_id)

      for syn in intv["other_names"]:
          syn_clean = syn.strip()
          syn_norm = syn_clean.lower()
          if syn_norm not in groups_raw[key]:
              groups_raw[key][syn_norm] = (syn_clean, nct_id)

      # Print drug traces
      if trace_drug and trace_drug.lower() == key:
          print(
              f"  [TRACE] NCT={intv['nct_id']} | "
              f"name='{intv['name']}' | "
              f"otherNames={intv['other_names']} | "
              f"group_so_far={groups_raw[key]}"
          )


  groups = {}
  for key, name_nct_map in groups_raw.items():
      sorted_items = sorted(name_nct_map.values(), key=lambda x: x[0].lower())
      canonical = next((orig for orig, _ in sorted_items if orig.lower() == key), sorted_items[0][0])

      groups[canonical] = {
          "synonyms": [orig for orig, _ in sorted_items],
          "nct_ids": [nct for _, nct in sorted_items],
      }

  return groups


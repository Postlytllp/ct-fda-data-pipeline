def groups_to_records(groups: dict) -> list:
    """
    Convert groups dict to a sorted list of record dicts.

    Args:
        groups: {canonical_name: [synonyms]} from deduplicate_drugs().

    Returns:
        Sorted list of {"canonical_name", "synonyms", "synonym_count"} dicts.
    """
    return sorted(
    [
        {
            "canonical_name": canonical,
            "synonyms": data["synonyms"],
            "nct_ids": data["nct_ids"],
            "synonym_count": len(data["synonyms"]),
        }
        for canonical, data in groups.items()
    ],
    key=lambda x: x["canonical_name"].lower(),
)


def save_json(records: list, filepath: str) -> str:
    """Save records to JSON file. Returns the filepath."""
    path = Path(filepath)
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(records, f, indent=2, ensure_ascii=False)
    return str(path)


def save_csv(records: list, filepath: str) -> str:
    """Save records to CSV file (synonyms pipe-separated). Returns filepath."""
    path = Path(filepath)
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["canonical_name", "synonyms", "synonym_count"])
        for drug in records:
            writer.writerow([
                drug["canonical_name"],
                " | ".join(drug["synonyms"]),
                drug["synonym_count"],
            ])
    return str(path)


def save_results(
    groups: dict,
    output_dir: str = ".",
    json_filename: str = "ctgov_drugs.json",
    csv_filename: str = "ctgov_drugs.csv",
    verbose: bool = True,
) -> tuple:
    """
    Save deduplicated drugs to JSON and CSV.

    Returns:
        (json_path, csv_path, records)
    """
    records = groups_to_records(groups)

    json_path = save_json(records, str(Path(output_dir) / json_filename))
    csv_path = save_csv(records, str(Path(output_dir) / csv_filename))

    if verbose:
        print(f"\nSaved {len(records)} unique drug entities:")
        print(f"  JSON: {json_path}")
        print(f"  CSV:  {csv_path}")

    return json_path, csv_path, records


def create_dataframe(records: list) -> pd.DataFrame:
    """
    Create a pandas DataFrame from records with a display-friendly synonyms column.

    Args:
        records: List of dicts from groups_to_records().

    Returns:
        DataFrame with columns: canonical_name, synonyms, synonym_count, synonyms_display
    """
    df = pd.DataFrame(records)
    if not df.empty:
        df["nct_ids_display"] = df["nct_ids"].apply(lambda x: " | ".join(x))
    return df


def print_summary(df: pd.DataFrame, elapsed: float):
    """Print a summary of the extraction results."""
    if df.empty:
        print("No drugs found.")
        return

    print(f"\n{'=' * 70}")
    print(f"COMPLETE in {elapsed:.1f}s ({elapsed / 60:.1f} min)")
    print(f"  Unique drug entities: {len(df)}")
    print(f"  Total name variants:  {df['synonym_count'].sum()}")
    print(f"  Avg synonyms/drug:    {df['synonym_count'].mean():.1f}")
    print(f"  Max synonyms:         {df['synonym_count'].max()}")

    top5 = df.nlargest(5, "synonym_count")[["canonical_name", "synonym_count"]]
    print(f"\n  Top 5 drugs by synonym count:")
    for _, row in top5.iterrows():
        print(f"    {row['canonical_name']}: {row['synonym_count']} synonyms")
    print(f"{'=' * 70}")


def download_in_colab(json_path: str, csv_path: str, verbose: bool = True):
    """Attempt to auto-download files if running in Google Colab."""
    try:
        from google.colab import files as colab_files
        if verbose:
            print("\nDownloading files to your local machine...")
        colab_files.download(json_path)
        colab_files.download(csv_path)
    except ImportError:
        pass  # Not in Colab

def extract_drug_names(df: pd.DataFrame, g_model, labels: list = None,
                       batch_size: int = 256, verbose: bool = True) -> pd.DataFrame:
    if labels is None:
        labels = ["CHE"]

    # Split into single-word (skip) vs multi-word (process)
    multi_word_mask = df["canonical_name"].str.strip().str.split().str.len() > 1
    multi_word_texts = df.loc[multi_word_mask, "canonical_name"].tolist()

    if verbose:
        print(f"  Skipping {(~multi_word_mask).sum()} single-word names")
        print(f"  Processing {len(multi_word_texts)} multi-word names in batches of {batch_size}")

    # Batch predict
    all_entities = []
    for i in range(0, len(multi_word_texts), batch_size):
        batch = multi_word_texts[i:i + batch_size]
        batch_results = g_model.inference (
            batch, labels, flat_ner=True, threshold=0.5
        )
        all_entities.extend(batch_results)

        if verbose:
            print(f"  Batch {i // batch_size + 1}: {min(i + batch_size, len(multi_word_texts))}/{len(multi_word_texts)}")

    # Map results back
    extracted = [[] for _ in range(len(df))]
    multi_indices = df.index[multi_word_mask].tolist()
    for idx, entities in zip(multi_indices, all_entities):
        if entities:
            extracted[idx] = list(dict.fromkeys(e["text"] for e in entities))

    df["drug_extracted"] = extracted
    df["drug_extracted_display"] = df["drug_extracted"].apply(lambda x: " | ".join(x))
    return df


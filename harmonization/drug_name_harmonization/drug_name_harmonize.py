def merge_by_extracted_drugs(df: pd.DataFrame, verbose: bool = True) -> pd.DataFrame:
    """
    Second-pass deduplication:
    1. Group rows sharing the same drug_extracted value
    2. Use the extracted drug name as the new canonical
    3. Merge all their synonyms and nct_ids
    """
    from collections import defaultdict

    # Build groups: extracted_drug (lowered) -> list of row indices
    extract_groups = defaultdict(list)

    # Step 1: Collect rows with exactly 1 extracted drug
    for i, row in df.iterrows():
        if len(row["drug_extracted"]) != 1:
            continue
        drug = row["drug_extracted"][0].strip().lower()
        extract_groups[drug].append(i)

    # Step 2: Also pull in rows whose canonical_name matches an extracted drug
    for drug_key in list(extract_groups.keys()):
        for i, row in df.iterrows():
            if i not in extract_groups[drug_key] and row["canonical_name"].strip().lower() == drug_key:
                extract_groups[drug_key].append(i)

    # Only care about groups with 2+ rows
    merge_groups = {k: list(dict.fromkeys(v)) for k, v in extract_groups.items() if len(set(v)) > 1}

    if verbose:
        print(f"  GLiNER merge: {len(merge_groups)} extracted drugs match multiple rows")

    if not merge_groups:
        return df

    # Track which rows have been consumed
    already_merged = set()
    rows_to_drop = []

    for extracted_drug, indices in merge_groups.items():
        # Skip if all indices already merged
        indices = [i for i in indices if i not in already_merged]
        if len(indices) < 2:
            continue

        # Find the canonical casing of the extracted drug
        # Prefer an index whose canonical_name exactly matches the extracted drug
        anchor_idx = None
        for i in indices:
            if df.at[i, "canonical_name"].strip().lower() == extracted_drug:
                anchor_idx = i
                break
        if anchor_idx is None:
            anchor_idx = indices[0]

        # Merge all rows into anchor
        combined_syns = []
        combined_ncts = []
        seen = set()

        for i in indices:
            for s, n in zip(df.at[i, "synonyms"], df.at[i, "nct_ids"]):
                if s.lower() not in seen:
                    seen.add(s.lower())
                    combined_syns.append(s)
                    combined_ncts.append(n)

        # Set the extracted drug as canonical name
        # Find original casing from the data
        canonical = df.at[indices[0], "drug_extracted"][0]

        df.at[anchor_idx, "canonical_name"] = canonical
        df.at[anchor_idx, "synonyms"] = combined_syns
        df.at[anchor_idx, "nct_ids"] = combined_ncts
        df.at[anchor_idx, "synonym_count"] = len(combined_syns)

        # Mark others for removal
        for i in indices:
            if i != anchor_idx:
                rows_to_drop.append(i)
                already_merged.add(i)

    df = df.drop(rows_to_drop).reset_index(drop=True)

    # Refresh display columns
    df["synonyms_display"] = df["synonyms"].apply(lambda x: " | ".join(x))
    df["nct_ids_display"] = df["nct_ids"].apply(lambda x: " | ".join(x))

    if verbose:
        print(f"  Dropped {len(rows_to_drop)} rows after merging")
        print(f"  Remaining: {len(df)} unique drug entities")

    return df


def build_api_params(
    page_size: int = 1000,
    status_filter: Optional[list] = None,
) -> dict:
    """
    Build query parameters for the ClinicalTrials.gov v2 API.

    Args:
        page_size: Number of studies per page (max 1000).
        status_filter: Optional list of statuses (e.g., ['RECRUITING']).

    Returns:
        Dict of query parameters.
    """
    params = {
        "format": "json",
        "pageSize": page_size,
        "fields": "|".join(API_FIELDS),
        "filter.advanced": "AREA[StudyType]INTERVENTIONAL",
    }
    if status_filter:
        params["filter.overallStatus"] = "|".join(status_filter)
    return params


def fetch_page(
    params: dict,
    page_token: Optional[str] = None,
    max_retries: int = 5,
    verbose: bool = True,
) -> dict:
    """
    Fetch a single page of studies from the CT.gov v2 API with retry logic.

    Args:
        params: Base query parameters from build_api_params().
        page_token: Pagination token for the next page (None for first page).
        max_retries: Number of retries on failure.
        verbose: Print retry/error messages.

    Returns:
        Parsed JSON response dict.

    Raises:
        RuntimeError: If all retries are exhausted.
    """
    request_params = dict(params)
    if page_token:
        request_params["pageToken"] = page_token
    elif "pageToken" in request_params:
        del request_params["pageToken"]

    for attempt in range(max_retries):
        try:
            resp = requests.get(API_BASE, params=request_params, timeout=60)
            if resp.status_code == 429:
                wait = 2 ** (attempt + 1)
                if verbose:
                    print(f"  Rate limited. Waiting {wait}s...")
                time.sleep(wait)
                continue
            resp.raise_for_status()
            return resp.json()
        except requests.exceptions.RequestException as e:
            if attempt < max_retries - 1:
                wait = 2 ** (attempt + 1)
                if verbose:
                    print(f"  Request error: {e}. Retrying in {wait}s...")
                time.sleep(wait)
            else:
                raise RuntimeError(f"Failed after {max_retries} attempts: {e}")

